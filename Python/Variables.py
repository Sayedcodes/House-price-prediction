#Variables

#rules of write a variable
#1. Variable names can only contain letters, numbers, and underscores.
#2. Variable names cannot start with a number.
#3. Variable names are case-sensitive.
#4. Variable names cannot be a Python keyword (like 'if', 'else', 'while', etc.).

#5. Variable names should be descriptive and meaningful.
#6. Variable names should not be too long or too short.
#7. Variable names should not contain spaces.
#8. Variable names should not contain special characters (like @, #, $, %, etc.).
#9. Variable names should not be the same as built-in functions (like 'print', 'len', 'str', etc.).

#Examples of valid variable names
my_variable = 10
myVariable = 20
my_variable_2 = 30
myVariable2 = 40
